    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        String aa, bb, cc;
        Double a, b, c;

        aa = textField1.getText();
        bb = textField2.getText();
        cc = textField3.getText();

        try {
            a = Double.parseDouble(aa);
            b = Double.parseDouble(bb);
            c = Double.parseDouble(cc);

            Double delta;

            delta = ((b * b) - (4 * a * c));

            String delta_string;
            String pierwszy_string;
            String drugi_string;
            String jeden_string;
            Double pierwszy = (-(b) - Math.sqrt(delta)) / 2 * a;
            Double drugi = (-(b) + Math.sqrt(delta)) / 2 * a;
            Double jeden = (-(b)) / 2 * a;

            if (a == 0) {
                if(b == 0){
                    if(c == 0){
                        textField9.setText("Brak");
                        textField10.setText("Nieskonczenie wiele");
                        textField13.setText("");
                        textField14.setText("");
                    }
                    else {
                        textField9.setText("Brak");
                        textField10.setText("0");
                        textField13.setText("Brak");
                        textField14.setText("Brak");
                    }
                }
                else {
                    textField9.setText("Brak");
                    textField10.setText("1");
                    textField13.setText("0");
                    textField14.setText("Brak");      
                }
            }
            else
            {
                    if (delta > 0) {
                        textField10.setText("2");
                        delta_string = Double.toString(delta);
                        textField9.setText(delta_string);
                        pierwszy_string = Double.toString(pierwszy);
                        drugi_string = Double.toString(drugi);
                        textField13.setText(pierwszy_string);
                        textField14.setText(drugi_string);
                    }
                    if (delta == 0) {
                        textField10.setText("1");
                        delta_string = Double.toString(delta);
                        textField9.setText(delta_string);
                        jeden_string = Double.toString(jeden);
                        textField13.setText(jeden_string);
                        textField14.setText(jeden_string);
                    }
                    if (delta < 0) {
                        textField10.setText("0");
                        delta_string = Double.toString(delta);
                        textField9.setText(delta_string);
                        textField13.setText("Brak");
                        textField14.setText("Brak");
                    }
            }
        } catch (Exception e) {
            textField9.setText("B≈ÇƒÖd");
            textField10.setText("B≈ÇƒÖd");
            textField13.setText("B≈ÇƒÖd");
            textField14.setText("B≈ÇƒÖd");
        }


    }//GEN-LAST:event_button1ActionPerformed

